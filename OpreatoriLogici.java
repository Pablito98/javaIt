public class OpreatoriLogici {
    public static void main(String[] args) {
        int x = 10;
        int y = 7;

        //confronti con operatori di confronto <, >, ==, !=

        boolean isTrue = x>y;

        boolean isTrue1 = x!=y;
        
        //operatori Logici
        boolean isTrue2 = x<7 || x>y;


        System.out.println(isTrue);
        System.out.println(isTrue1);
        System.out.println(isTrue2);
    }
}
